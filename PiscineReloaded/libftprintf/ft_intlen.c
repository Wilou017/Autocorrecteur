#include "libft.h"

int		ft_intlen(t_intmax nb)
{
	int			len;
	t_uintmax	n;

	len = 0;
	n = nb;
	if (nb < 0)
	{
		len = 1;
		n = -nb;
	}
	while (n > 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}

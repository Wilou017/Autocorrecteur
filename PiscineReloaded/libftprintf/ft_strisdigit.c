#include <libft.h>

int	ft_strisdigit(char const *s1)
{
	int i;

	i = 0;
	if (s1[i] == '-')
		i++;
	while (s1[i])
	{
		if (!ft_isdigit(s1[i]))
			return (0);
		i++;
	}
	return (1);
}
